package com.collections;

public class CollectionTask1 {
	public int id;
	public String name;
	
	public CollectionTask1(int id, String name)
	{
		this.setId(id);
		this.setName(name);
	}
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

}
